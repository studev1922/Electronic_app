/* ACCOUNTS AND AUTHORIZATIONS */
-- Create USERS table
IF OBJECT_ID('USERS', 'U') IS NOT NULL DROP TABLE [USERS]
GO
CREATE TABLE [USERS]
(
   -- use email
   [username] varchar(20) primary key,
   [password] binary(70) not null,
   [email] varchar(100) not null unique,
   [name] nvarchar(100) not null,
   -- soft delete
   [active] smallint default 0, -- -1:soft-delete | 0:lock | 1:open
   [regTime] datetime default GETDATE(),
   [image] varchar(80) default 'default.png'
);
GO

-- Create ROLES table
IF OBJECT_ID('ROLES', 'U') IS NOT NULL DROP TABLE [ROLES]
GO
CREATE TABLE [ROLES]
(
   [rid] int primary key,
   [role] nvarchar(50) unique not null
);
GO

-- Create AUTHS table
IF OBJECT_ID('AUTHS', 'U') IS NOT NULL DROP TABLE [AUTHS]
GO
CREATE TABLE [AUTHS]
(
   [u_id] varchar(20) foreign key references [USERS]([username])
    on update cascade on delete cascade not null,
   [r_id] int foreign key references [ROLES]([rid]) not null,
   primary key ([u_id], [r_id])
);
GO

/* PRODUCTS AND CLASSIFICATION */
-- Create PRODUCTS table
IF OBJECT_ID('CATEGORIES', 'U') IS NOT NULL DROP TABLE [CATEGORIES]
GO
CREATE TABLE [CATEGORIES]
(
   [cgid] int primary key,
   [name] nvarchar(50) not null,
   [image] varchar(80) default 'default.png'
);
GO

-- Create PRODUCTS table
IF OBJECT_ID('PRODUCTS', 'U') IS NOT NULL DROP TABLE [PRODUCTS]
GO
CREATE TABLE [PRODUCTS]
(
   [prid] bigint identity primary key,
   [name] nvarchar(150) not null,
   [note] nvarchar(256),
   [active] smallint default 0, -- -1:soft-delete | 0:lock | 1:open
   [price] float default 0,
   [quantity] int default 100,
   [regTime] datetime default GETDATE(),
   [image] varchar(80) default 'default.png',
   [cg_id] int foreign key references [CATEGORIES]([cgid]),
   [u_id] varchar(20) foreign key references [USERS]([username])
   on update cascade on delete cascade
);
GO

-- Create ORDERS table
IF OBJECT_ID('ORDERS', 'U') IS NOT NULL DROP TABLE [ORDERS]
GO
CREATE TABLE [ORDERS]
(
   [orid] bigint identity primary key,
   [note] nvarchar(255),
   [regTime] datetime default GETDATE(),
   [buyer_id] varchar(20) foreign key references [USERS]([username]), -- buyer
   [staff_id] varchar(20) foreign key references [USERS]([username])
   on update cascade on delete cascade not null -- staff seller
);
GO

-- Create ORDER_DETAILS table
IF OBJECT_ID('ORDER_DETAILS', 'U') IS NOT NULL DROP TABLE [ORDER_DETAILS]
GO
CREATE TABLE [ORDER_DETAILS]
(
   [quantity] tinyint default 1,
   [oldPrice] float default 0,
   [pr_id] bigint foreign key references [PRODUCTS]([prid]),
   [or_id] bigint foreign key references [ORDERS]([orid])
   on update cascade on delete cascade not null, -- in order cart
   primary key([pr_id], [or_id])
);
GO


